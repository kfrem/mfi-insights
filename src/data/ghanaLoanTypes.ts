// Helper functions
export function getAllLoanProducts(): LoanProduct[] {
  return LOAN_CATEGORIES.flatMap((cat) => cat.products);
}

export function getLoanProductsByCategory(categoryCode: string): LoanProduct[] {
  const category = LOAN_CATEGORIES.find((c) => c.code === categoryCode);
  return category?.products || [];
}

export function getLoanProduct(productCode: string): LoanProduct | undefined {
  return getAllLoanProducts().find((p) => p.code === productCode);
}

export function getCategoryOptions(): { value: string; label: string }[] {
  return LOAN_CATEGORIES.map((cat) => ({
    value: cat.code,
    label: cat.name,
  }));
}

export function getProductOptions(categoryCode?: string): { value: string; label: string; description: string }[] {
  const products = categoryCode ? getLoanProductsByCategory(categoryCode) : getAllLoanProducts();
  return products.map((p) => ({
    value: p.code,
    label: p.name,
    description: p.description,
  }));
}

